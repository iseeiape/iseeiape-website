// Run a specific job
function runJob(jobName) {
  const jobConfig = CONFIG.jobs[jobName];
  
  if (!jobConfig) {
    console.error(`❌ Job not found: ${jobName}`);
    console.log('Available jobs:', Object.keys(CONFIG.jobs).join(', '));
    process.exit(1);
  }
  
  if (!jobConfig.enabled) {
    console.error(`❌ Job is disabled: ${jobName}`);
    process.exit(1);
  }
  
  console.log(`🚀 Running job: ${jobName}`);
  console.log(`📝 ${jobConfig.description}`);
  console.log(`⏰ Schedule: ${jobConfig.schedule}`);
  
  const jobFile = path.join(CONFIG.jobsDir, `${jobName}.js`);
  
  if (!fs.existsSync(jobFile)) {
    console.error(`❌ Job file not found: ${jobFile}`);
    console.log('Run "node scripts/cron-manager.js setup" first');
    process.exit(1);
  }
  
  try {
    const result = execSync(`node "${jobFile}"`, {
      encoding: 'utf8',
      stdio: 'inherit',
      cwd: path.join(__dirname, '..')
    });
    
    console.log(`✅ Job ${jobName} completed`);
    
  } catch (error) {
    console.error(`❌ Job ${jobName} failed:`, error.message);
    process.exit(1);
  }
}

// List all jobs
