// Ensure directories exist
function ensureDirectories() {
  [CONFIG.jobsDir, CONFIG.logsDir].forEach(dir => {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
      console.log(`📁 Created directory: ${dir}`);
    }
  });
}

// Create job files
function createJobFiles() {
  console.log('📝 Creating job files...');
  
  Object.entries(CONFIG.jobs).forEach(([jobName, jobConfig]) => {
    if (!jobConfig.enabled) return;
    
    const jobFile = path.join(CONFIG.jobsDir, `${jobName}.js`);
    
    const jobContent = `#!/usr/bin/env node

/**
 * ${jobConfig.description}
 * 
 * Generated by Cron Manager
 * Schedule: ${jobConfig.schedule.replace(/\*\//g, '*\\/')}
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const LOGS_DIR = path.join(__dirname, '../../neo-crypto/logs');
const JOB_NAME = '${jobName}';
const TIMESTAMP = new Date().toISOString().replace(/[:.]/g, '-');

// Ensure logs directory exists
if (!fs.existsSync(LOGS_DIR)) {
  fs.mkdirSync(LOGS_DIR, { recursive: true });
}

function log(message, level = 'INFO') {
  const logMessage = \`[\${new Date().toISOString()}] [\${level}] \${message}\\n\`;
  const logFile = path.join(LOGS_DIR, \`\${JOB_NAME}-\${TIMESTAMP}.log\`);
  
  fs.appendFileSync(logFile, logMessage);
  console.log(logMessage.trim());
}

function logError(error) {
  log(\`Error: \${error.message}\`, 'ERROR');
  if (error.stack) {
    log(\`Stack: \${error.stack}\`, 'ERROR');
  }
}

async function main() {
  log(\`Starting \${JOB_NAME} job\`);
  log(\`Schedule: ${jobConfig.schedule}\`);
  log(\`Description: ${jobConfig.description}\`);
  
  try {
    const startTime = Date.now();
    
    // Execute the main script
    const scriptPath = path.join(__dirname, '../../${jobConfig.script}');
    const args = ${JSON.stringify(jobConfig.args || [])};
    
    log(\`Executing: node \${scriptPath} \${args.join(' ')}\`);
    
    const result = execSync(
      \`node "\${scriptPath}" \${args.join(' ')}\`,
      { 
        encoding: 'utf8',
        stdio: ['pipe', 'pipe', 'pipe'],
        cwd: path.join(__dirname, '../../')
      }
    );
    
    const executionTime = Date.now() - startTime;
    
    log(\`Execution completed in \${executionTime}ms\`);
    log(\`Output: \${result.substring(0, 500)}\${result.length > 500 ? '...' : ''}\`);
    
    // Check for errors in output
    if (result.includes('❌') || result.includes('Error:') || result.includes('error:')) {
      log('Potential errors detected in output', 'WARN');
    }
    
    log(\`Job \${JOB_NAME} completed successfully\`);
    
  } catch (error) {
    logError(error);
    process.exit(1);
  }
}

// Run if called directly
if (require.main === module) {
  main().catch(error => {
    logError(error);
    process.exit(1);
  });
}

module.exports = { main };
`;
    
    fs.writeFileSync(jobFile, jobContent, 'utf8');
    fs.chmodSync(jobFile, '755'); // Make executable
    
    console.log(`  ✅ Created: ${jobFile}`);
  });
  
  console.log(`\n📋 Created ${Object.values(CONFIG.jobs).filter(j => j.enabled).length} job files`);
}

// Create systemd service file (for production)
function createSystemdService() {
  const serviceContent = `[Unit]
Description=iseeiape Neo Crypto Engine Cron Manager
After=network.target

[Service]
Type=simple
User=${process.env.USER || 'matrix'}
WorkingDirectory=${path.join(__dirname, '..')}
ExecStart=/usr/bin/node ${path.join(__dirname, 'cron-manager.js')} start
Restart=on-failure
RestartSec=10

[Install]
WantedBy=multi-user.target
`;
  
  const serviceFile = path.join(CONFIG.jobsDir, 'iseeiape-cron.service');
  fs.writeFileSync(serviceFile, serviceContent, 'utf8');
  
  console.log(`📋 Created systemd service: ${serviceFile}`);
  console.log('\nTo install:');
  console.log(`  sudo cp ${serviceFile} /etc/systemd/system/`);
  console.log('  sudo systemctl daemon-reload');
  console.log('  sudo systemctl enable iseeiape-cron');
  console.log('  sudo systemctl start iseeiape-cron');
}

