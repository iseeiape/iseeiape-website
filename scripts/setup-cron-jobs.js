#!/usr/bin/env node
/**
 * Setup Cron Jobs for Wolf Pack Automation
 * Creates scheduled tasks for daily content generation and performance updates
 */

const fs = require('fs');
const path = require('path');
const { exec } = require('child_process');
const { promisify } = require('util');

const execAsync = promisify(exec);

// Configuration
const PROJECT_ROOT = path.join(__dirname, '..');
const SCRIPTS_DIR = path.join(__dirname);
const LOGS_DIR = path.join(PROJECT_ROOT, 'logs');

// Ensure logs directory exists
if (!fs.existsSync(LOGS_DIR)) {
  fs.mkdirSync(LOGS_DIR, { recursive: true });
}

// Cron job definitions
const CRON_JOBS = [
  {
    name: 'wolf-pack-daily-content',
    schedule: '0 9 * * *', // 9:00 AM daily (Europe/Bucharest)
    command: `cd ${PROJECT_ROOT} && node ${path.join(SCRIPTS_DIR, 'content-automation.js')}`,
    description: 'Generate daily Wolf Pack performance content'
  },
  {
    name: 'wolf-pack-performance-update',
    schedule: '*/30 * * * *', // Every 30 minutes
    command: `cd ${PROJECT_ROOT} && node ${path.join(SCRIPTS_DIR, 'performance-updater.js')}`,
    description: 'Update Wolf Pack performance metrics'
  },
  {
    name: 'website-health-check',
    schedule: '0 */6 * * *', // Every 6 hours
    command: `cd ${PROJECT_ROOT} && node ${path.join(SCRIPTS_DIR, 'health-check.js')}`,
    description: 'Check website health and send alerts'
  },
  {
    name: 'backup-performance-db',
    schedule: '0 0 * * *', // Midnight daily
    command: `cd ${PROJECT_ROOT} && node ${path.join(SCRIPTS_DIR, 'backup-database.js')}`,
    description: 'Backup Wolf Pack performance database'
  }
];

async function setupCronJobs() {
  console.log('🔄 Setting up Wolf Pack automation cron jobs...\n');
  
  // Check if crontab is available
  try {
    await execAsync('which crontab');
  } catch (error) {
    console.error('❌ crontab not found. Please install cron on your system.');
    process.exit(1);
  }
  
  // Get current crontab
  let currentCrontab = '';
  try {
    const { stdout } = await execAsync('crontab -l 2>/dev/null || echo ""');
    currentCrontab = stdout;
  } catch (error) {
    console.log('📝 No existing crontab found, creating new one...');
  }
  
  // Remove existing Wolf Pack jobs
  const lines = currentCrontab.split('\n').filter(line => {
    return !line.includes('# WOLF-PACK-AUTO') && 
           !line.includes('wolf-pack-') &&
           !line.includes('iseeiape-');
  });
  
  // Add new jobs
  const newJobs = CRON_JOBS.map(job => {
    const logFile = path.join(LOGS_DIR, `${job.name}.log`);
    const errorFile = path.join(LOGS_DIR, `${job.name}.error.log`);
    
    return [
      `# WOLF-PACK-AUTO: ${job.description}`,
      `${job.schedule} ${job.command} >> ${logFile} 2>> ${errorFile}`
    ].join('\n');
  });
  
  // Combine all jobs
  const newCrontab = [
    '# ──────────────────────────────────────────────────────────────',
    '# 🐺 WOLF PACK AUTOMATION CRON JOBS',
    '# Generated by setup-cron-jobs.js',
    '# Do not edit manually - regenerate with setup-cron-jobs.js',
    '# ──────────────────────────────────────────────────────────────',
    '',
    ...newJobs,
    '',
    '# ──────────────────────────────────────────────────────────────',
    '# End of Wolf Pack automation jobs',
    '# ──────────────────────────────────────────────────────────────',
    '',
    ...lines.filter(line => line.trim() !== ''),
    ''  // Ensure newline at end
  ].join('\n');
  
  // Write new crontab
  const tempFile = '/tmp/wolf-pack-crontab.txt';
  fs.writeFileSync(tempFile, newCrontab);
  
  try {
    await execAsync(`crontab ${tempFile}`);
    console.log('✅ Cron jobs installed successfully!\n');
    
    // Show installed jobs
    console.log('📋 Installed Jobs:');
    CRON_JOBS.forEach(job => {
      console.log(`   • ${job.name}: ${job.schedule}`);
      console.log(`     ${job.description}`);
    });
    
    console.log(`\n📁 Logs directory: ${LOGS_DIR}`);
    console.log('🔍 Check logs with:');
    console.log(`   tail -f ${path.join(LOGS_DIR, 'wolf-pack-daily-content.log')}`);
    
    // Create a simple monitoring script
    createMonitoringScript();
    
  } catch (error) {
    console.error('❌ Failed to install cron jobs:', error.message);
    process.exit(1);
  }
}

function createMonitoringScript() {
  const monitorScript = path.join(SCRIPTS_DIR, 'monitor-cron-jobs.sh');
  
  const scriptContent = `#!/bin/bash
# Wolf Pack Cron Jobs Monitor

LOGS_DIR="${LOGS_DIR}"
SCRIPTS_DIR="${SCRIPTS_DIR}"

echo "🐺 Wolf Pack Cron Jobs Monitor"
echo "================================"
echo ""

# Check if jobs are running
echo "📋 Checking cron jobs..."
crontab -l | grep -A1 "WOLF-PACK-AUTO"

echo ""
echo "📊 Log Files:"
echo "------------"

for job in wolf-pack-daily-content wolf-pack-performance-update website-health-check backup-performance-db; do
  log_file="\${LOGS_DIR}/\${job}.log"
  error_file="\${LOGS_DIR}/\${job}.error.log"
  
  if [ -f "\$log_file" ]; then
    size=\$(stat -c%s "\$log_file")
    lines=\$(wc -l < "\$log_file")
    last_run=\$(tail -1 "\$log_file" 2>/dev/null | cut -c1-50)
    
    echo "• \${job}:"
    echo "  Size: \${size} bytes, Lines: \${lines}"
    echo "  Last: \${last_run}"
    
    if [ -f "\$error_file" ] && [ \$(stat -c%s "\$error_file") -gt 0 ]; then
      errors=\$(wc -l < "\$error_file")
      echo "  ⚠️  Errors: \${errors} (check \${error_file})"
    else
      echo "  ✅ No errors"
    fi
  else
    echo "• \${job}: No log file yet"
  fi
  echo ""
done

# Show recent logs
echo "🔄 Recent Activity:"
echo "------------------"
tail -20 "\${LOGS_DIR}/wolf-pack-performance-update.log" 2>/dev/null || echo "No performance updates yet"

echo ""
echo "💡 Commands:"
echo "  • View all logs: ls -la \${LOGS_DIR}/"
echo "  • Follow daily content: tail -f \${LOGS_DIR}/wolf-pack-daily-content.log"
echo "  • Check errors: grep -i error \${LOGS_DIR}/*.error.log"
echo "  • Run manually: cd ${PROJECT_ROOT} && node scripts/content-automation.js"
`;

  fs.writeFileSync(monitorScript, scriptContent);
  fs.chmodSync(monitorScript, '755');
  
  console.log(`\n📊 Monitoring script created: ${monitorScript}`);
  console.log(`   Run: ${monitorScript}`);
}

// Create supporting scripts
function createSupportingScripts() {
  console.log('\n📝 Creating supporting scripts...');
  
  // Performance updater
  const performanceUpdater = path.join(SCRIPTS_DIR, 'performance-updater.js');
  if (!fs.existsSync(performanceUpdater)) {
    fs.writeFileSync(performanceUpdater, `#!/usr/bin/env node
/**
 * Performance Updater Script
 * Updates Wolf Pack performance metrics
 */

const { exec } = require('child_process');
const path = require('path');

console.log('🔄 Updating Wolf Pack performance metrics...');

// Run the fix script
const fixScript = path.join(__dirname, '../../fix_performance_tracker.py');
const command = \`python3 \${fixScript}\`;

exec(command, (error, stdout, stderr) => {
  if (error) {
    console.error('❌ Error updating performance:', error.message);
    console.error('stderr:', stderr);
    return;
  }
  
  console.log('✅ Performance metrics updated successfully');
  console.log(stdout);
});
`);
    fs.chmodSync(performanceUpdater, '755');
    console.log(`   Created: ${performanceUpdater}`);
  }
  
  // Health check
  const healthCheck = path.join(SCRIPTS_DIR, 'health-check.js');
  if (!fs.existsSync(healthCheck)) {
    fs.writeFileSync(healthCheck, `#!/usr/bin/env node
/**
 * Website Health Check
 */

const https = require('https');
const { exec } = require('child_process');
const { promisify } = require('util');

const execAsync = promisify(exec);

async function checkWebsite() {
  console.log('🏥 Checking website health...');
  
  const checks = [
    { name: 'Main Site', url: 'https://iseeiape.com' },
    { name: 'API Health', url: 'https://iseeiape.com/api/health' },
    { name: 'Dashboard', url: 'https://iseeiape.com/dashboard-enhanced-v3' }
  ];
  
  for (const check of checks) {
    try {
      const response = await fetch(check.url);
      const status = response.status;
      
      if (status >= 200 && status < 300) {
        console.log(\`  ✅ \${check.name}: HTTP \${status}\`);
      } else {
        console.log(\`  ⚠️  \${check.name}: HTTP \${status}\`);
      }
    } catch (error) {
      console.log(\`  ❌ \${check.name}: \${error.message}\`);
    }
  }
  
  // Check disk space
  try {
    const { stdout } = await execAsync('df -h . | tail -1');
    console.log(\`  💾 Disk: \${stdout.trim()}\`);
  } catch (error) {
    console.log(\`  ⚠️  Disk check failed: \${error.message}\`);
  }
  
  console.log('✅ Health check completed');
}

checkWebsite().catch(console.error);
`);
    fs.chmodSync(healthCheck, '755');
    console.log(`   Created: ${healthCheck}`);
  }
  
  // Backup database
  const backupDb = path.join(SCRIPTS_DIR, 'backup-database.js');
  if (!fs.existsSync(backupDb)) {
    fs.writeFileSync(backupDb, `#!/usr/bin/env node
/**
 * Backup Wolf Pack Performance Database
 */

const fs = require('fs');
const path = require('path');
const { exec } = require('child_process');
const { promisify } = require('util');

const execAsync = promisify(exec);

const DB_PATH = path.join(__dirname, '../../wolf_performance.db');
const BACKUP_DIR = path.join(__dirname, '../../backups');
const DATE = new Date().toISOString().split('T')[0];
const BACKUP_PATH = path.join(BACKUP_DIR, \`wolf-performance-\${DATE}.db\`);

async function backupDatabase() {
  console.log('💾 Backing up Wolf Pack performance database...');
  
  // Ensure backup directory exists
  if (!fs.existsSync(BACKUP_DIR)) {
    fs.mkdirSync(BACKUP_DIR, { recursive: true });
  }
  
  // Check if database exists
  if (!fs.existsSync(DB_PATH)) {
    console.log('❌ Database not found:', DB_PATH);
    return;
  }
  
  // Create backup
  try {
    await execAsync(\`cp "\${DB_PATH}" "\${BACKUP_PATH}"\`);
    console.log(\`✅ Backup created: \${BACKUP_PATH}\`);
    
    // Clean old backups (keep last 7 days)
    const files = fs.readdirSync(BACKUP_DIR)
      .filter(f => f.startsWith('wolf-performance-') && f.endsWith('.db'))
      .map(f => ({ name: f, path: path.join(BACKUP_DIR, f), time: fs.statSync(path.join(BACKUP_DIR, f)).mtime }))
      .sort((a, b) => b.time - a.time);
    
    if (files.length > 7) {
      const toDelete = files.slice(7);
      toDelete.forEach(file => {
        fs.unlinkSync(file.path);
        console.log(\`🗑️  Deleted old backup: \${file.name}\`);
      });
    }
    
    console.log(\`📊 Backups kept: \${Math.min(files.length, 7)}/\${files.length}\`);
    
  } catch (error) {
    console.error('❌ Backup failed:', error.message);
  }
}

backupDatabase().catch(console.error);
`);
    fs.chmodSync(backupDb, '755');
    console.log(`   Created: ${backupDb}`);
  }
}

// Run setup
async function main() {
  try {
    createSupportingScripts();
    await setupCronJobs();
    
    console.log('\n🎉 Setup complete!');
    console.log('\nNext steps:');
    console.log('1. Review the cron jobs with: crontab -l');
    console.log('2. Test content generation: node scripts/content-automation.js');
    console.log('3. Monitor with: ./scripts/monitor-cron-jobs.sh');
    console.log('\n🦎 The Wolf Pack automation system is now ready!');
    
  } catch (error) {
    console.error('❌ Setup failed:', error);
    process.exit(1);
  }
}

if (require.main === module) {
  main();
}

module.exports = { setupCronJobs };