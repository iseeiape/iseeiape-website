// Helper functions
function readJSON(filePath) {
  try {
    if (fs.existsSync(filePath)) {
      return JSON.parse(fs.readFileSync(filePath, 'utf8'));
    }
  } catch (error) {
    console.error(`❌ Error reading ${filePath}:`, error.message);
  }
  return null;
}

function writeFile(filePath, content) {
  try {
    fs.writeFileSync(filePath, content, 'utf8');
    console.log(`✅ Written to ${filePath}`);
    return true;
  } catch (error) {
    console.error(`❌ Error writing to ${filePath}:`, error.message);
    return false;
  }
}

function ensureDirectory(dir) {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
    console.log(`📁 Created directory: ${dir}`);
  }
}

function getCurrentTimestamp() {
  return new Date().toISOString().replace(/[:.]/g, '-');
}

function loadTemplate(templateName) {
  const templatePath = path.join(CONFIG.templatesDir, templateName);
  try {
    if (fs.existsSync(templatePath)) {
      return fs.readFileSync(templatePath, 'utf8');
    }
  } catch (error) {
    console.error(`❌ Error loading template ${templateName}:`, error.message);
  }
  
  // Return default template if specific one doesn't exist
  return `# {{title}}

## Overview
{{overview}}

## Key Insights
{{insights}}

## Analysis
{{analysis}}

## Recommendations
{{recommendations}}

## Conclusion
{{conclusion}}

---
*Generated by Neo Crypto Engine • ${new Date().toISOString().split('T')[0]}*`;
}

function analyzeWolfPackData(wolfData) {
  if (!wolfData || !wolfData.recentAlerts || wolfData.recentAlerts.length === 0) {
    return null;
  }
  
  const analysis = {
    topAlerts: wolfData.recentAlerts.slice(0, 5),
    trends: wolfData.analysis?.trends || [],
    topTokens: wolfData.analysis?.topTokens || [],
    recommendations: wolfData.recommendations || [],
    summary: wolfData.analysis?.summary || {}
  };
  
  // Calculate overall market sentiment
  const highConfidenceAlerts = analysis.topAlerts.filter(a => a.confidence >= 80);
  const newPairs = analysis.topAlerts.filter(a => a.category === 'newpairs');
  const momentumAlerts = analysis.topAlerts.filter(a => a.category === 'momentum' || a.category === 'breakout');
  
  analysis.sentiment = {
    overall: highConfidenceAlerts.length >= 2 ? 'bullish' : 
             analysis.topAlerts.length >= 3 ? 'neutral' : 'cautious',
    newPairsCount: newPairs.length,
    momentumCount: momentumAlerts.length,
    highConfidenceCount: highConfidenceAlerts.length
  };
  
  return analysis;
}

function generateAlertAnalysis(wolfAnalysis, template) {
  if (!wolfAnalysis || wolfAnalysis.topAlerts.length === 0) {
    return null;
  }
  
  const topAlert = wolfAnalysis.topAlerts[0];
  const timestamp = new Date().toISOString().split('T')[0];
  
  // Generate title
  const title = `Wolf Pack Alert Analysis: ${topAlert.symbol} ${topAlert.emoji} - ${timestamp}`;
  
  // Generate overview
  const overview = `The Wolf Pack system has detected significant activity for **${topAlert.symbol}** with a confidence score of **${topAlert.confidence}%**. This alert falls under the **${topAlert.category}** category and represents one of ${wolfAnalysis.topAlerts.length} high-priority signals in the current scan.`;
  
  // Generate insights
  const insights = [
    `**${topAlert.symbol}** scored ${topAlert.score} points in the Wolf Pack analysis`,
    `Alert type: ${topAlert.type || 'General alert'}`,
    `Category: ${topAlert.category} (${wolfAnalysis.summary.byCategory?.[topAlert.category] || 1} similar alerts)`,
    `Overall market sentiment: ${wolfAnalysis.sentiment.overall}`,
    `Total alerts in current scan: ${wolfAnalysis.summary.totalAlerts || wolfAnalysis.topAlerts.length}`
  ];
  
  // Generate analysis
  const similarAlerts = wolfAnalysis.topAlerts.filter(a => a.symbol === topAlert.symbol);
  let analysis = `**${topAlert.symbol}** has generated ${similarAlerts.length} alert${similarAlerts.length > 1 ? 's' : ''} in the current Wolf Pack scan. `;
  
  if (topAlert.category === 'newpairs') {
    analysis += `This represents a **new trading pair** with early momentum. New pairs often present opportunities for early entry but carry higher risk.`;
  } else if (topAlert.category === 'momentum' || topAlert.category === 'breakout') {
    analysis += `This indicates **strong momentum** and potential breakout conditions. Momentum alerts suggest sustained buying pressure and increasing attention.`;
  } else if (topAlert.category === 'alpha') {
    analysis += `This is an **alpha signal** - high-confidence detection of undervalued or mispriced opportunities. Alpha alerts represent the highest conviction signals from the Wolf Pack system.`;
  }
  
  // Add trend context if available
  if (wolfAnalysis.trends && wolfAnalysis.trends.length > 0) {
    const relatedTrend = wolfAnalysis.trends.find(t => t.symbol === topAlert.symbol);
    if (relatedTrend) {
      analysis += `\n\n**Trend Context:** ${topAlert.symbol} has shown up in ${relatedTrend.alertCount} alert${relatedTrend.alertCount > 1 ? 's' : ''} with an average score of ${relatedTrend.avgScore}, indicating a **sustained trend** with ${relatedTrend.strength}% strength.`;
    }
  }
  
  // Generate recommendations
  const recommendations = [];
  
  if (topAlert.confidence >= 80) {
    recommendations.push(`**Immediate Research:** ${topAlert.symbol} shows high confidence (${topAlert.confidence}%). Conduct quick due diligence on tokenomics, team, and recent developments.`);
  }
  
  if (topAlert.category === 'newpairs') {
    recommendations.push(`**Liquidity Check:** Monitor initial liquidity and volume growth. New pairs with sustained volume above $100k often indicate genuine interest.`);
  }
  
  if (wolfAnalysis.sentiment.highConfidenceCount >= 2) {
    recommendations.push(`**Market Context:** Multiple high-confidence alerts (${wolfAnalysis.sentiment.highConfidenceCount}) suggest broader market momentum. Consider portfolio rebalancing to capture trend.`);
  }
  
  // Generate conclusion
  let conclusion = `The Wolf Pack alert for **${topAlert.symbol}** represents a ${topAlert.confidence >= 80 ? 'high-confidence' : 'moderate-confidence'} opportunity in the **${topAlert.category}** category. `;
  
  if (wolfAnalysis.sentiment.overall === 'bullish') {
    conclusion += `With overall bullish sentiment and ${wolfAnalysis.sentiment.highConfidenceCount} high-confidence alerts, the current market environment favors proactive positioning.`;
  } else {
    conclusion += `Exercise appropriate risk management while monitoring for confirmation signals.`;
  }
  
  // Replace template variables
  let content = template
    .replace(/{{title}}/g, title)
    .replace(/{{overview}}/g, overview)
    .replace(/{{insights}}/g, insights.map(i => `- ${i}`).join('\n'))
    .replace(/{{analysis}}/g, analysis)
    .replace(/{{recommendations}}/g, recommendations.map(r => `- ${r}`).join('\n'))
    .replace(/{{conclusion}}/g, conclusion);
  
  return {
    title,
    content,
    metadata: {
      type: 'alert-analysis',
      symbol: topAlert.symbol,
      confidence: topAlert.confidence,
      category: topAlert.category,
      generatedAt: new Date().toISOString(),
      dataSource: 'wolf-pack'
    }
  };
}

function generateTrendReport(wolfAnalysis, marketData, template) {
  if (!wolfAnalysis || !wolfAnalysis.trends || wolfAnalysis.trends.length === 0) {
    return null;
  }
  
  const timestamp = new Date().toISOString().split('T')[0];
  const topTrends = wolfAnalysis.trends.slice(0, 3);
  
  // Generate title
  const title = `Crypto Trend Report: ${timestamp} - ${topTrends.map(t => t.symbol).join(', ')}`;
  
  // Generate overview
  const overview = `The Wolf Pack system has identified **${wolfAnalysis.trends.length} sustained trends** in the current market, with ${topTrends.length} showing particularly strong signals. This report analyzes the most significant trends and provides actionable insights for traders and investors.`;
  
  // Generate insights
  const insights = [
    `**Top Trends:** ${topTrends.map(t => `${t.symbol} (${t.strength}%)`).join(', ')}`,
    `**Total Alerts:** ${wolfAnalysis.summary.totalAlerts || 0} alerts processed`,
    `**Market Sentiment:** ${wolfAnalysis.sentiment.overall}`,
    `**High Confidence Signals:** ${wolfAnalysis.sentiment.highConfidenceCount}`,
    `**New Pairs Detected:** ${wolfAnalysis.sentiment.newPairsCount}`
  ];
  
  // Generate analysis for each trend
  let analysis = `## Trend Analysis\n\n`;
  
  topTrends.forEach((trend, index) => {
    analysis += `### ${index + 1}. ${trend.symbol} - ${trend.strength}% Trend Strength\n\n`;
    analysis += `- **Alert Count:** ${trend.alertCount} alert${trend.alertCount > 1 ? 's' : ''}\n`;
    analysis += `- **Average Score:** ${trend.avgScore}/100\n`;
    analysis += `- **Categories:** ${trend.categories.join(', ')}\n`;
    analysis += `- **Trend Interpretation:** `;
    
    if (trend.strength >= 80) {
      analysis += `Very strong sustained trend. Multiple high-confidence alerts indicate genuine market interest and momentum.\n\n`;
    } else if (trend.strength >= 60) {
      analysis += `Moderate to strong trend. Consistent alerts suggest developing narrative and growing attention.\n\n`;
    } else {
      analysis += `Early-stage trend. Monitor for additional confirmation signals and volume growth.\n\n`;
    }
  });
  
  // Add market context if available
  if (marketData && marketData.summary) {
    analysis += `## Market Context\n\n`;
    analysis += `- **Total Tokens Tracked:** ${marketData.summary.totalTokens || 'N/A'}\n`;
    analysis += `- **24h Volume:** $${(marketData.summary.totalVolume24h / 1000000).toFixed(1)}M\n`;
    
    if (marketData.summary.topGainer) {
      analysis += `- **Top Gainer:** ${marketData.summary.topGainer.symbol} (+${marketData.summary.topGainer.priceChange24h?.toFixed(1) || 'N/A'}%)\n`;
    }
  }
  
  // Generate recommendations
  const recommendations = [];
  
  // Recommendation based on strongest trend
  if (topTrends.length > 0) {
    const strongest = topTrends[0];
    recommendations.push(`**Focus on ${strongest.symbol}:** As the strongest trend (${strongest.strength}%), allocate research resources to understand the underlying drivers and potential catalysts.`);
  }
  
  // Recommendation based on trend diversity
  const trendCategories = new Set();
  topTrends.forEach(t => t.categories.forEach(c => trendCategories.add(c)));
  if (trendCategories.size > 1) {
    recommendations.push(`**Diversified Trends:** Trends span ${trendCategories.size} different categories (${Array.from(trendCategories).join(', ')}), indicating broad-based market activity rather than isolated movements.`);
  }
  
  // Risk management recommendation
  if (wolfAnalysis.sentiment.highConfidenceCount >= 3) {
    recommendations.push(`**Aggressive Positioning:** With ${wolfAnalysis.sentiment.highConfidenceCount} high-confidence signals, consider more aggressive positioning while maintaining strict stop-losses.`);
  } else {
    recommendations.push(`**Cautious Approach:** Limited high-confidence signals suggest a more selective, risk-managed approach to position sizing.`);
  }
  
  // Generate conclusion
  const conclusion = `The current market shows **${wolfAnalysis.trends.length} identifiable trends** with varying strength levels. `;
  
  if (wolfAnalysis.sentiment.overall === 'bullish') {
    conclusion += `Overall bullish sentiment combined with ${wolfAnalysis.sentiment.highConfidenceCount} high-confidence signals creates a favorable environment for trend-following strategies.`;
  } else if (wolfAnalysis.sentiment.overall === 'neutral') {
    conclusion += `Neutral sentiment suggests selective opportunities exist, but require careful timing and position management.`;
  } else {
    conclusion += `Cautious sentiment indicates a wait-and-see approach may be prudent until clearer trends emerge.`;
  }
  
  conclusion += ` Monitor the identified trends for confirmation and be prepared to adjust as new Wolf Pack alerts arrive.`;
  
  // Replace template variables
  let content = template
    .replace(/{{title}}/g, title)
    .replace(/{{overview}}/g, overview)
    .replace(/{{insights}}/g, insights.map(i => `- ${i}`).join('\n'))
    .replace(/{{analysis}}/g, analysis)
    .replace(/{{recommendations}}/g, recommendations.map(r => `- ${r}`).join('\n'))
    .replace(/{{conclusion}}/g, conclusion);
  
  return {
    title,
    content,
    metadata: {
      type: 'trend-report',
      trends: topTrends.map(t => t.symbol),
      trendCount: wolfAnalysis.trends.length,
      generatedAt: new Date().toISOString(),
      dataSource: 'wolf-pack'
    }
  };
}

function selectContentType(availableData) {
  const availableTypes = [];
  
  // Check which content types we can generate based on available data
  for (const [typeId, typeConfig] of Object.entries(CONFIG.contentTypes)) {
    const hasRequiredData = typeConfig.requires.every(req => {
      if (req === 'wolfPackData') return availableData.wolfData;
      if (req === 'marketData') return availableData.marketData;
      return false;
    });
    
    if (hasRequiredData) {
      availableTypes.push({
        id: typeId,
        ...typeConfig,
        effectivePriority: typeConfig.priority * (Math.random() * 0.3 + 0.85) // Add some randomness
      });
    }
  }
  
  // Sort by effective priority
  availableTypes.sort((a, b) => b.effectivePriority - a.effectivePriority);
  
  return availableTypes.length > 0 ? availableTypes[0] : null;
}

async function main() {
  console.log('🚀 Enhanced Content Generator - Neo Crypto Engine\n');
  
  // Ensure output directory exists
  ensureDirectory(CONFIG.outputsDir);
  
  // Load data sources
  console.log('📊 Loading data sources...');
  const wolfData = readJSON(CONFIG.wolfPackData);
  const marketData = readJSON(CONFIG.marketData);
  
  if (!wolfData && !marketData) {
    console.log('❌ No data available for content generation');
    return;
  }
  
  console.log(`✅ Wolf Pack data: ${wolfData ? 'Loaded' : 'Not available'}`);
  console.log(`✅ Market data: ${marketData ? 'Loaded' : 'Not available'}`);
  
  // Analyze data
  const wolfAnalysis = wolfData ? analyzeWolfPackData(wolfData) : null;
  
  // Select content type to generate
  const availableData = { wolfData, marketData };
  const selectedType = selectContentType(availableData);
  
  if (!selectedType) {
    console.log('❌ No suitable content type can be generated with available data');
    return;
  }
  
  console.log(`\n🎯 Selected content type: ${selectedType.name}`);
  console.log(`📝 Description: ${selectedType.description}`);
  
  // Load template
  const template = loadTemplate(selectedType.template);
  
  // Generate content based on type
  let generatedContent = null;
  
  switch (selectedType.id) {
    case 'alert-analysis':
      generatedContent = generateAlertAnalysis(wolfAnalysis, template);
      break;
      
    case 'trend-report':
      generatedContent = generateTrendReport(wolfAnalysis, marketData, template);
      break;
      
    case 'token-deep-dive':
      // TODO: Implement token deep dive
      console.log('⚠️  Token deep dive not yet implemented');
      return;
      
    case 'market-outlook':
      // TODO: Implement market outlook
      console.log('⚠️  Market outlook not yet implemented');
      return;
      
    default:
      console.log(`❌ Unknown content type: ${selectedType.id}`);
      return;
  }
  
  if (!generatedContent) {
    console.log('❌ Failed to generate content');
    return;
  }
  
  // Save the generated content
  const timestamp = getCurrentTimestamp();
  const filename = `${selectedType.id}-${timestamp}.md`;
  const outputPath = path.join(CONFIG.outputsDir, filename);
  
  const saved = writeFile(outputPath, generatedContent.content);
  
  if (saved) {
    console.log(`\n✅ Successfully generated: ${filename}`);
    console.log(`📁 Location: ${outputPath}`);
    console.log(`📊 Metadata:`);
    console.log(`  • Type: ${generatedContent.metadata.type}`);
    console.log(`  • Generated: ${generatedContent.metadata.generatedAt}`);
    
    if (generatedContent.metadata.symbol) {
      console.log(`  • Symbol: ${generatedContent.metadata.symbol}`);
      console.log(`  • Confidence: ${generatedContent.metadata.confidence}%`);
    }
    
    if (generatedContent.metadata.trends) {
      console.log(`  • Trends: ${generatedContent.metadata.trends.join(', ')}`);
    }
    
    // Also save metadata separately
    const metaFilename = `${selectedType.id}-${timestamp}-meta.json`;
    const metaPath = path.join(CONFIG.outputsDir, metaFilename);
    writeFile(metaPath, JSON.stringify(generatedContent.metadata, null, 2));
    
    console.log(`\n📋 Content Preview:`);
    console.log(`---\n${generatedContent.title}\n---`);
    console.log(generatedContent.content.substring(0, 300) + '...');
  }
}

// Run if called directly
if (require.main === module) {
  main().catch(error => {
    console.error('❌ Fatal error:', error);
    process.exit(1);
  });
}

